package com.company;

public class DanoveSazbyStatu {

    private String zkratkaStatu;

    private String jmenoStatu;

    private Float plnaDanovaSazba;

    private Float snizenaDanovaSazba;

    private Boolean specialniDanovaSazba;

    public String getZkratkaStatu() {
        return zkratkaStatu;
    }

    public void setZkratkaStatu(String zkratkaStatu) {
        this.zkratkaStatu = zkratkaStatu;
    }

    public String getJmenoStatu() {
        return jmenoStatu;
    }

    public void setJmenoStatu(String jmenoStatu) {
        this.jmenoStatu = jmenoStatu;
    }

    public Float getPlnaDanovaSazba() {
        return plnaDanovaSazba;
    }

    public void setPlnaDanovaSazba(Float plnaDanovaSazba) {
        this.plnaDanovaSazba = plnaDanovaSazba;
    }

    public Float getSnizenaDanovaSazba() {
        return snizenaDanovaSazba;
    }

    public void setSnizenaDanovaSazba(Float snizenaDanovaSazba) {
        this.snizenaDanovaSazba = snizenaDanovaSazba;
    }

    public Boolean getSpecialniDanovaSazba() {
        return specialniDanovaSazba;
    }

    public void setSpecialniDanovaSazba(Boolean specialniDanovaSazba) {
        this.specialniDanovaSazba = specialniDanovaSazba;
    }

}
